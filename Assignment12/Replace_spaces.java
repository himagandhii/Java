//Program to replace the spaces of a string with a specific character.

public class Replace_spaces
{
    public static void main(String[] args)
    {
        String s = "Hy, today is a beautiful day.";
        System.out.println(s);
        System.out.println("Replaced value: " + s.replace(" ","*"));  //replaces ch with new char
    }
}
