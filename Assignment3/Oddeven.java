class Oddeven      //Program to check whether given number is even or odd
{
    public static void main(String[] args)
    {
        int number = 50;
        if (number % 2 == 0)
        {
            System.out.println("Given number is even.");
        }
        else
        {
            System.out.println("Given number is odd.");
        }
    }
}